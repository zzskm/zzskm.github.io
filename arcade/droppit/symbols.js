function validateIconDefinition(icon) {
  if (!icon || typeof icon !== "object") {
    throw new Error("Icon definition must be an object.");
  }
  if (!icon.name || typeof icon.name !== "string") {
    throw new Error("Icon must have a string name.");
  }
  if (!Array.isArray(icon.px)) {
    throw new Error(`${icon.name}: px must be an array.`);
  }
  if (icon.px.length !== DROP_PIT_ICON_PIXELS) {
    throw new Error(`${icon.name}: px length must be ${DROP_PIT_ICON_PIXELS}, got ${icon.px.length}.`);
  }
  for (let i = 0; i < icon.px.length; i++) {
    const value = icon.px[i];
    if (!Number.isInteger(value) || value < 0 || value >= DROP_PIT_PALETTE.length) {
      throw new Error(`${icon.name}: invalid palette index at ${i} -> ${value}`);
    }
  }
}

function makeIcon(icon) {
  validateIconDefinition(icon);
  return Object.freeze({
    name: icon.name,
    w: DROP_PIT_ICON_W,
    h: DROP_PIT_ICON_H,
    palette: DROP_PIT_PALETTE,
    px: new Uint8Array(icon.px),
  });
}

const DROP_PIT_SYMBOLS = Object.freeze(DROP_PIT_RAW_SYMBOLS.map(makeIcon));
const DROP_PIT_SYMBOL_MAP = Object.freeze(
  DROP_PIT_SYMBOLS.reduce((acc, icon) => {
    acc[icon.name] = icon;
    return acc;
  }, {})
);

const DROP_PIT_RENDER_SPAN_CACHE = new WeakMap();

function buildDropPitIconSpans(icon, transparentIndex) {
  const w = icon.w;
  const h = icon.h;
  const px = icon.px;
  const buckets = new Array(icon.palette.length);

  for (let y = 0; y < h; y++) {
    const row = y * w;
    let x = 0;
    while (x < w) {
      const idx = px[row + x];
      if (idx === transparentIndex) {
        x++;
        continue;
      }

      const start = x;
      x++;
      while (x < w && px[row + x] === idx) x++;

      if (!buckets[idx]) buckets[idx] = [];
      buckets[idx].push(start, y, x - start);
    }
  }

  return buckets;
}

function getDropPitIconSpans(icon, transparentIndex) {
  let byTransparentIndex = DROP_PIT_RENDER_SPAN_CACHE.get(icon);
  if (!byTransparentIndex) {
    byTransparentIndex = new Map();
    DROP_PIT_RENDER_SPAN_CACHE.set(icon, byTransparentIndex);
  }

  let spans = byTransparentIndex.get(transparentIndex);
  if (!spans) {
    spans = buildDropPitIconSpans(icon, transparentIndex);
    byTransparentIndex.set(transparentIndex, spans);
  }
  return spans;
}

function renderDropPitIconToCanvas(ctx, icon, scale = 1, transparentIndex = 0) {
  if (!ctx || !icon) return;
  const s = Math.max(1, scale | 0);
  const t = transparentIndex | 0;
  const spansByColor = getDropPitIconSpans(icon, t);
  ctx.imageSmoothingEnabled = false;

  for (let idx = 0; idx < spansByColor.length; idx++) {
    const spans = spansByColor[idx];
    if (!spans || !spans.length) continue;

    ctx.fillStyle = icon.palette[idx];
    for (let i = 0; i < spans.length; i += 3) {
      const x = spans[i];
      const y = spans[i + 1];
      const run = spans[i + 2];
      ctx.fillRect(x * s, y * s, run * s, s);
    }
  }
}

